int chagall_emc_init(void)
{
	struct board_info board;

	tegra_get_board_info(&board);

	printk(KERN_INFO "%s(): board_id = %x, board_sku = %x\n", __FUNCTION__, board.board_id, board.sku);

	switch (board.board_id) {
	case BOARD_E1291:
		if (MEMORY_TYPE(board.sku) == SKU_MEMORY_SAMSUNG_K4P8G304EB_FGC1) {
			printk(KERN_WARNING "%s(): SKU_MEMORY_SAMSUNG_K4P8G304EB_FGC1\n", __FUNCTION__);
			tegra_init_emc(chagall_emc_tables_k4p8g304eb_fgc1,
					ARRAY_SIZE(chagall_emc_tables_k4p8g304eb_fgc1));
		}	else if (MEMORY_TYPE(board.sku) == SKU_MEMORY_ELPIDA_EDB8132B2MA_8D_F) {
			printk(KERN_WARNING "%s(): SKU_MEMORY_ELPIDA_EDB8132B2MA_8D_F\n", __FUNCTION__);
			tegra_init_emc(chagall_emc_tables_edb8132b2ma,
					ARRAY_SIZE(chagall_emc_tables_edb8132b2ma));
		}	else if (MEMORY_TYPE(board.sku) == SKU_MEMORY_SAMSUNG_K4P8G304EB_FGC2) {
			printk(KERN_WARNING "%s(): SKU_MEMORY_SAMSUNG_K4P8G304EB_FGC2\n", __FUNCTION__);
			tegra_init_emc(chagall_emc_tables_k4p8g304eb_fgc2,
					ARRAY_SIZE(chagall_emc_tables_k4p8g304eb_fgc2));
		} else if (MEMORY_TYPE(board.sku) == SKU_MEMORY_HYNIX_H9TCNNN8JDMMPR_NGM) {
			printk(KERN_WARNING "%s(): SKU_MEMORY_HYNIX_H9TCNNN8JDMMPR_NGM\n", __FUNCTION__);
			tegra_init_emc(chagall_emc_tables_h9tcnnn8jdmmpr,
					ARRAY_SIZE(chagall_emc_tables_h9tcnnn8jdmmpr));
		} else {
			printk(KERN_WARNING "%s(): Can't identify board sku, disable DVFT!\n", __FUNCTION__);
		}
		break;

	default:
		break;
	}

	return 0;
}
